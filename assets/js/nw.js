  function scrollToSectiona(sectionId) {
    const targetElement = document.getElementById(sectionId);
    if (targetElement) {
        window.scrollTo({
            top: targetElement.offsetTop,
            behavior: 'smooth'
        });
    } else {
        console.error(`Element with id ${sectionId} not found.`);
    }
  }
  
